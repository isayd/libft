#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int a;

	i = ft_strlen(s1);
	a = 0;
	while (s2[a] != '\0')
	{
		s1[i] = s2[a];
		a++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
