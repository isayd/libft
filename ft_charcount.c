#include "libft.h"

int	ft_charcount(char *s1)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (s1)
	{
		while (s1[i] != '\0')
		{
			if ((s1[i] >= 'a' && s1[i] <= 'z') ||
				(s1[i] >= 'A' && s1[i] <= 'Z'))
				j++;
			i++;
		}
		return (j);
	}
	return (0);
}
