#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int number;
	int sign_unsigned;

	i = 0;
	number = 0;
	sign_unsigned = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign_unsigned = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = (number * 10) + (str[i] - '0');
		i++;
	}
	return (number * sign_unsigned);
}
