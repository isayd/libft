#include "libft.h"

int	ft_sccount(char *s1)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (s1)
	{
		while (s1[i] != '\0')
		{
			if ((s1[i] >= '!' && s1[i] <= '/') || (s1[i] >= ':' &&
				s1[i] <= '@') || (s1[i] >= '[' && s1[i] <= '`') ||
				(s1[i] >= '{' && s1[i] <= '~'))
				j++;
			i++;
		}
		return (j);
	}
	return (0);
}
