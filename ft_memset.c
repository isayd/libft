#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char *a;

	a = b;
	if (len != 0)
	{
		while (len > 0)
		{
			*a++ = (unsigned char)c;
			len--;
		}
	}
	return (b);
}
