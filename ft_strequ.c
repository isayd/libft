#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int i;

	i = 0;
	if (s1 && s2)
	{
		i = ft_strcmp(s1, s2);
		if (!i)
			return (1);
		if (s1)
			return (0);
	}
	return (0);
}
